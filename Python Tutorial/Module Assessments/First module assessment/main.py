first_number = input("Enter then 1st number:")

print("The 1st number is:", first_number)

second_number = input("Enter the 2nd number:")

print("The 2nd number is:", second_number)

sum = int(first_number) + int(second_number)
subraction = int(first_number) - int(second_number)
multiplication = int(first_number) * int(second_number)
division = int(first_number) / int(second_number)


print("The sum of the two numbers:", sum)
print("The difference of the two numbers:", subraction)
print("The product of the two numbers:", multiplication)
print("The quotient of the two numbers:", division)

